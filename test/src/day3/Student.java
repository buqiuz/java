package day3;

public class Student {
    int id;
    String name;
    int age;
    String major;
    int grade;

    public Student(){}

    public Student(int id, String name, int age, String major, int grade) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.major = major;
        this.grade = grade;
    }
}
